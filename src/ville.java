
public class ville {
    private int idVille;
    private String nom;
	
    
    public ville(int idVille, String nom) {
		this.idVille = idVille;
		this.nom = nom;
	}


	public int getIdVille() {
		return idVille;
	}


	public void setIdVille(int idVille) {
		this.idVille = idVille;
	}


	public String getNom() {
		return nom;
	}


	public void setNom(String nom) {
		this.nom = nom;
	}

}
